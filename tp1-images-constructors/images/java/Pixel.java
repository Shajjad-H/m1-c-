

/**
 * Pixel
 */
public class Pixel {
    protected int r, g, b;

    public Pixel() {

        this.r = 0;
        this.g = 0;
        this.b = 0;
    }

    public Pixel(int r, int g, int b) {

        this.r = r;
        this.g = g;
        this.b = b;
    }


    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    public int getG() {
        return g;
    }

    public void setG(int g) {
        this.g = g;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }


    
}